document.write("<h2>Variable</h2>");


// 1. 變數(variable)

// 變數要先存在才能使用，所以要先宣告(declare)變數
// 變數沒有給初始值(initial value)，印出來就是 undefined(未定義)


var x;
var y = 5;

document.write("x: " + x + "<br/>");
document.write("y: " + y + "<br/>");
document.write("<br/>");





// 2. 變數給值(assign)

// 可以用變數把給定的值存起來
// 用等號左邊的變數把等號右邊的值存起來

var z;
var w = 10;

document.write("z: " + z + "<br/>");

z = 20;
document.write("z: " + z + "<br/>");

z = w;
document.write("z: " + z + "<br/>");

document.write("<br/>");



